"""## Activation Functions

"""

import math
def sigmoid(z):
  a = 1 / (1+math.exp(-z))
  return a

def reLu(z):
  a = max(0, z)
  return a

"""## Forward Pass"""

def neuron(x1, x2, w1, w2, b):
  z = w1*x1 + w2*x2 + b;
  return z;

def forward_pass(x1, x2):
  z11 = neuron(x1, x2, w11, w12, b1)
  a11 = reLu(z11)
  z12 = neuron(x1, x2, w21, w22, b2)
  a12 = reLu(z12)
  z31 = neuron(a11, a12, w31, w32, b3)
  output = sigmoid(z31)
  return output

outputs = []
def calc_forward_pass(students):
  for student in students:
    outputs.append(forward_pass(student[0], student[1]))
  return outputs

calc_forward_pass(students)

