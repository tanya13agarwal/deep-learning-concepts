# Forward pass for ALL students at once!
def forward_pass_batch(X):
    """Forward pass for multiple students in one go"""

    # Hidden layer
    # X @ W_hidden.T means: each row of X times W_hidden transposed
    # np.dot() is matrix multiplication of 2d array while @ handles all types of array matrix multiplication
    # .T means "transpose" (flip rows and columns)
    Z_hidden = X @ W_hidden.T + b_hidden
    print(f"Z_hidden: {Z_hidden}")
    H = relu_np(Z_hidden)
    print(f"\nH: {H}")

    # Output layer
    Z_output = H @ W_output + b_output
    print(f"\nZ_output: {Z_output}")
    # since 1D array, z_output will automatically be transposed
    print(f"z_output shape: {Z_output.shape}")
    y_hat = sigmoid_np(Z_output)
    print(f"y_hat: {y_hat}")

    return y_hat


# Get predictions for all 4 students AT THE SAME TIME!
