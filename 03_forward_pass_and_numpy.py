# Vectorized activation functions
def sigmoid_np(x):
    """Sigmoid that works on NumPy arrays"""
    return 1 / (1 + np.exp(-x))

def relu_np(x):
    """ReLU that works on NumPy arrays"""
    return np.maximum(0, x)   # np.maximum works element-by-element


def forward_pass_numpy(x):
    """Complete forward pass using NumPy"""

    # Hidden layer: W × x + b, then ReLU
    z_hidden = np.dot(W_hidden, x) + b_hidden
    print(f"z_hidden: {z_hidden}")
    h = relu_np(z_hidden)
    print(f"neuron value after activation: {h}")

    # Output layer: W × h + b, then Sigmoid
    z_output = np.dot(W_output, h) + b_output
    print(f"z_output: {z_output}")
    y_hat = sigmoid_np(z_output)
    print(f"output value after activation: {y_hat}")

    return y_hat


