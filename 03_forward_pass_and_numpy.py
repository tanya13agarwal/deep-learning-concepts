# Remember:- to make code consistent, and use transpose everywhere, we convert 1D arrays into 2D arrays

# in numpy, * is normal element wise multiplication - both arrays should have same shape
# @ is matrix multiplication - sum of all multiplicated values - shape:- (mXp) @ (pXn) = (mXn)

def cross_entropy(y, predictions):
  epsilon = 1e-10
  # predictions = np.maximum(epsilon, np.minimum(1-epsilon, predictions))
  # we cannot use above because epsilon is a single value and predictions is array therefore we use clip
  predictions = np.clip(predictions, epsilon, 1-epsilon)
  # since y and predictions have same shape, use normal element wise multiplication
  loss = - ( y * np.log(predictions) + (1-y) * np.log(1-predictions) )
  # doing batch training, therefore average of all losses calculated
  loss = np.mean(loss)
  return loss

cross_entropy(y, predictions)

