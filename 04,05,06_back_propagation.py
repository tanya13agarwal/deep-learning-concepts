import numpy as np
import matplotlib.pyplot as plt
np.random.seed(42)

# ---------- DATA ----------
X = np.array([[2,7],[5,6],[8,5],[1,4]], dtype=float)  # (4,2) students × features
y = np.array([[0],[1],[1],[0]], dtype=float)          # (4,1) targets

# ---------- NORMALIZE ----------
# axis=0 → per column (per feature). Centers data around 0, spread 1.
X_mean = X.mean(axis=0)
X_std = X.std(axis=0)
X = (X - X_mean) / X_std

# ---------- INIT WEIGHTS ----------
# Small RANDOM (not zero → break symmetry so each neuron learns differently)
W_hidden = np.random.randn(2, 2) * 0.5   # (2 neurons, 2 inputs)
b_hidden = np.zeros(2)                     # (2,)
W_output = np.random.randn(1, 2) * 0.5   # (1 neuron, 2 inputs)
b_output = np.zeros(1)                     # (1,)

# ---------- ACTIVATIONS ----------
def reLu(Z): return np.maximum(0, Z)
def sigmoid(Z): return 1/(1 + np.exp(-Z))

# ---------- LOSS ----------
def loss(y, y_pred):
    epsilon = 1e-10
    y_pred = np.clip(y_pred, epsilon, 1-epsilon)   # avoid log(0)
    return np.mean(-(y*np.log(y_pred) + (1-y)*np.log(1-y_pred)))

# ---------- DERIVATIVES (brute force, kept separate!) ----------
def loss_derivative(y, y_pred):
    m = X.shape[0]
    return (-y/y_pred + (1-y)/(1-y_pred)) / m   # dL/dy_pred (averaged)

def sigmoid_derivative(z):
    return sigmoid(z) * (1 - sigmoid(z))        # dy_pred/dz_out

def reLu_derivative(z):
    return (z > 0).astype(float)                # da/dz_hidden

def z_derivative_via_w_or_a(i):
    return i      # dz/dw = input,  dz/da = weight

def z_derivative_via_b():
    return 1      # dz/db = 1

# ---------- FORWARD ----------
def forward_pass():
    Z_hidden = X @ W_hidden.T + b_hidden    # (4,2)@(2,2)+(2,) = (4,2)
    a = reLu(Z_hidden)                        # (4,2)
    Z_output = a @ W_output.T + b_output      # (4,2)@(2,1)+(1,) = (4,1)
    y_pred = sigmoid(Z_output)                # (4,1)
    L = loss(y, y_pred)
    return Z_hidden, a, Z_output, y_pred, L

Z_hidden, a, Z_output, y_pred, L = forward_pass()
print("y_pred:\n", np.round(y_pred, 4))
print("Loss:", round(L, 4))

# ---------- BACKWARD (brute force) ----------
learning_rate = 0.5

def back_propagation(X, y, y_pred, Z_output, a, Z_hidden):
    global W_hidden, b_hidden, W_output, b_output

    # === OUTPUT LAYER ===
    dL_dy_pred = loss_derivative(y, y_pred)          # (4,1)
    dy_pred_dz_output = sigmoid_derivative(Z_output) # (4,1)
    # combine along chain → element-wise (same shape, per student)
    delta_output = dL_dy_pred * dy_pred_dz_output    # (4,1)

    dz_output_dw_output = z_derivative_via_w_or_a(a) # = a, (4,2)
    # gradient = delta × input, SUMMED over students → transpose!
    # (1,4) @ (4,2) = (1,2)  [students summed away]
    dL_dw_output = delta_output.T @ dz_output_dw_output   # (1,2)
    dL_db_output = np.sum(delta_output, axis=0)            # (1,)

    # === HIDDEN LAYER ===
    dz_output_da = z_derivative_via_w_or_a(W_output) # = W_output, (1,2)
    # spread error back through output weights → dot product (shapes match, no .T)
    # (4,1) @ (1,2) = (4,2)
    delta_temp = delta_output @ dz_output_da         # (4,2)

    da_dz_hidden = reLu_derivative(Z_hidden)         # (4,2)
    # gate the error → element-wise (same shape)
    delta_hidden = delta_temp * da_dz_hidden         # (4,2)

    dz_hidden_dw_hidden = z_derivative_via_w_or_a(X) # = X, (4,2)
    # gradient = delta × input, SUMMED over students → transpose!
    # (2,4) @ (4,2) = (2,2)
    dL_dw_hidden = dz_hidden_dw_hidden.T @ delta_hidden  # (2,2)
    dL_db_hidden = np.sum(delta_hidden, axis=0)          # (2,)

    # === UPDATE ALL PARAMETERS ===
    W_output = W_output - learning_rate * dL_dw_output
    b_output = b_output - learning_rate * dL_db_output
    W_hidden = W_hidden - learning_rate * dL_dw_hidden
    b_hidden = b_hidden - learning_rate * dL_db_hidden

back_propagation(X, y, y_pred, Z_output, a, Z_hidden)
print("\nAfter one step:")
_, _, _, y_pred_new, L_new = forward_pass()
print("New loss:", round(L_new, 4), "(was", round(L, 4), ")")

# Re-initialize for a clean run
np.random.seed(42)
W_hidden = np.random.randn(2, 2) * 0.5
b_hidden = np.zeros(2)
W_output = np.random.randn(1, 2) * 0.5
