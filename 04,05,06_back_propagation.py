learning_rate = 0.5
epochs = 1000
loss_history = []

for epoch in range(epochs):
    # 1. FORWARD
    Z_hidden, a, Z_output, y_pred, L = forward_pass()
    loss_history.append(L)

    # 2. BACKWARD (updates all weights & biases)
    back_propagation(X, y, y_pred, Z_output, a, Z_hidden)

    if epoch % 100 == 0:
        print(f"Epoch {epoch:4d} | Loss: {L:.4f}")

print(f"\nFinal loss: {loss_history[-1]:.4f}")

# Plot the learning curve
